#include "libft.h"

int	ft_cnt_digits(int n)
{
	int	cnt;

	cnt = 1;
	if (n < 0)
	{
		cnt++;
		if (n /= 10)
			cnt++;
		n *= -1;
	}
	while (n /= 10)
		cnt++;
	return (cnt);
}
